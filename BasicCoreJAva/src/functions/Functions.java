package functions;

public class Functions {

	
	public static int sum(int a, int b)
	{
		
		return a+b;
	}
	
	public static int multiply(int a, int b)
	{
		return a*b;
	}
	
	public int interestCalc(int p , int n, int r)
	{
		return (p*n*r)/100;
	}
	
	public int mod(int a, int b)
	{
		return a%b;
	}
}
